CREATE TABLE COMBINE_OBJECT
(
  ID INTEGER PRIMARY KEY,
  SHAPE INTEGER,
  WIDTH INTEGER,
  HEIGHT INTEGER
);

CREATE TABLE SIMPLE_OBJECT
(
  ID INTEGEREGER PRIMARY KEY NOT NULL,
  SHAPE INTEGER NOT NULL,
  WIDTH INTEGER NOT NULL,
  HEIGHT INTEGER NOT NULL,
  COLOR INTEGER NOT NULL,
  PATTERN INTEGER NOT NULL,
  PATTERN_COLOR INTEGER NOT NULL,
  PARENTCOMBINEDOBJECT INTEGER,
  FOREIGN KEY (PARENTCOMBINEDOBJECT) REFERENCES COMBINE_OBJECT(ID)
);

CREATE TABLE SYMBOL
(
  ID INTEGER PRIMARY KEY NOT NULL,
  SHAPE INTEGER NOT NULL,
  WIDTH INTEGER NOT NULL,
  HEIGHT INTEGER NOT NULL,
  COLOR INTEGER NOT NULL,
  PARENTSIMPLEOBJECT INTEGER NOT NULL,
  FOREIGN KEY (PARENTSIMPLEOBJECT) REFERENCES SIMPLE_OBJECT(ID)
);

